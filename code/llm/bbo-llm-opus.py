
import numpy as np
from scipy.stats import norm
from scipy.optimize import minimize
from scipy.spatial.distance import cdist
import warnings
warnings.filterwarnings('ignore')

class GaussianProcess:
    """Gaussian Process surrogate model with RBF kernel"""
    
    def __init__(self, length_scale=1.0, noise=1e-6):
        self.length_scale = length_scale
        self.noise = noise
        self.X_train = None
        self.y_train = None
        self.K_inv = None
        
    def rbf_kernel(self, X1, X2):
        """RBF (Squared Exponential) kernel"""
        dists = cdist(X1 / self.length_scale, X2 / self.length_scale, metric='sqeuclidean')
        return np.exp(-0.5 * dists)
    
    def fit(self, X, y):
        """Fit the GP to training data"""
        self.X_train = np.array(X)
        self.y_train = np.array(y).reshape(-1, 1)
        
        # Compute kernel matrix with noise
        K = self.rbf_kernel(self.X_train, self.X_train)
        K += self.noise * np.eye(len(K))
        
        # Store inverse for predictions
        self.K_inv = np.linalg.inv(K)
        
    def predict(self, X):
        """Predict mean and std at new points"""
        X = np.array(X)
        if X.ndim == 1:
            X = X.reshape(1, -1)
            
        K_s = self.rbf_kernel(X, self.X_train)
        K_ss = self.rbf_kernel(X, X)
        
        # Mean prediction
        mu = K_s @ self.K_inv @ self.y_train
        
        # Variance prediction
        var = K_ss - K_s @ self.K_inv @ K_s.T
        std = np.sqrt(np.maximum(np.diag(var), 1e-10))
        
        return mu.flatten(), std


class BayesianOptimizer:
    """Bayesian Optimization for black-box function minimization"""
    
    def __init__(self, X_init, y_init, bounds, minimize=True):
        """
        X_init: Initial input data (n_samples, n_features)
        y_init: Initial output data (n_samples,)
        bounds: List of (min, max) tuples for each dimension
        minimize: If True, minimize; if False, maximize
        """
        self.X = np.array(X_init)
        self.y = np.array(y_init).flatten()
        self.bounds = np.array(bounds)
        self.minimize = minimize
        self.n_dims = self.X.shape[1]
        
        # Initialize and fit GP
        self.gp = GaussianProcess(length_scale=1.0)
        self.gp.fit(self.X, self.y)
        
    def expected_improvement(self, X):
        """Calculate Expected Improvement acquisition function"""
        X = np.array(X).reshape(-1, self.n_dims)
        mu, std = self.gp.predict(X)
        
        if self.minimize:
            best_y = np.min(self.y)
            improvement = best_y - mu
        else:
            best_y = np.max(self.y)
            improvement = mu - best_y
            
        Z = improvement / (std + 1e-10)
        ei = improvement * norm.cdf(Z) + std * norm.pdf(Z)
        ei[std < 1e-10] = 0.0
        
        return ei
    
    def suggest_next(self, n_restarts=10):
        """Suggest next point to evaluate using EI"""
        best_x = None
        best_ei = -np.inf
        
        for _ in range(n_restarts):
            # Random starting point
            x0 = np.random.uniform(self.bounds[:, 0], self.bounds[:, 1])
            
            # Optimize EI
            result = minimize(
                lambda x: -self.expected_improvement(x)[0],
                x0,
                bounds=self.bounds,
                method='L-BFGS-B'
            )
            
            ei_val = -result.fun
            if ei_val > best_ei:
                best_ei = ei_val
                best_x = result.x
                
        return best_x, best_ei
    
    def update(self, X_new, y_new):
        """Update with new observation"""
        self.X = np.vstack([self.X, X_new])
        self.y = np.append(self.y, y_new)
        self.gp.fit(self.X, self.y)
        
    def get_best(self):
        """Return best observed point"""
        if self.minimize:
            idx = np.argmin(self.y)
        else:
            idx = np.argmax(self.y)
        return self.X[idx], self.y[idx]


class MultiFunctionBayesianOptimizer:
    """Manages Bayesian Optimization for multiple black-box functions"""
    
    def __init__(self):
        self.optimizers = {}
        self.function_data = {}
        
    def add_function(self, func_id, X_data, y_data, bounds=None, minimize=True):
        """
        Add a black-box function with its initial data
        
        func_id: Identifier (1-8)
        X_data: Input data array
        y_data: Output data array
        bounds: Optional bounds, inferred from data if not provided
        """
        X = np.array(X_data)
        y = np.array(y_data).flatten()
        
        # Infer bounds from data with some margin
        if bounds is None:
            margin = 0.1
            ranges = X.max(axis=0) - X.min(axis=0)
            bounds = list(zip(
                X.min(axis=0) - margin * ranges,
                X.max(axis=0) + margin * ranges
            ))
        
        self.function_data[func_id] = {'X': X, 'y': y, 'bounds': bounds}
        self.optimizers[func_id] = BayesianOptimizer(X, y, bounds, minimize)
        
        print(f"Function {func_id}: {X.shape[0]} samples, {X.shape[1]} dimensions")
        
    def suggest_next(self, func_id):
        """Get next suggested point for a function"""
        return self.optimizers[func_id].suggest_next()
    
    def update(self, func_id, X_new, y_new):
        """Update a function with new observation"""
        self.optimizers[func_id].update(X_new, y_new)
        
    def get_best(self, func_id):
        """Get best observed point for a function"""
        return self.optimizers[func_id].get_best()
    
    def optimize_all(self, black_box_functions, n_iterations=10):
        """
        Run optimization for all functions
        
        black_box_functions: Dict mapping func_id to callable
        n_iterations: Number of optimization iterations
        """
        results = {}
        
        for func_id, func in black_box_functions.items():
            print(f"\n{'='*50}")
            print(f"Optimizing Function {func_id}")
            print(f"{'='*50}")
            
            for i in range(n_iterations):
                # Get suggestion
                x_next, ei = self.suggest_next(func_id)
                
                # Evaluate black-box function
                y_next = func(x_next)
                
                # Update model
                self.update(func_id, x_next, y_next)
                
                best_x, best_y = self.get_best(func_id)
                print(f"Iter {i+1}: EI={ei:.4f}, y_new={y_next:.4f}, best_y={best_y:.4f}")
            
            results[func_id] = self.get_best(func_id)
            
        return results
    
    def summary(self):
        """Print summary of all functions"""
        print("\n" + "="*60)
        print("OPTIMIZATION SUMMARY")
        print("="*60)
        for func_id in sorted(self.optimizers.keys()):
            best_x, best_y = self.get_best(func_id)
            n_samples = len(self.optimizers[func_id].y)
            print(f"Function {func_id}: best_y = {best_y:.4f} (n_samples={n_samples})")


